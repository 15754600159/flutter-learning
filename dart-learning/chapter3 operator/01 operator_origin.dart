/**
 * 原始值操作符：
 *  1. 算数元素符：+ - * / ~/ %
 *  2. 关系运算符：== != > < >= <=
 *  3. 逻辑运算符：! && ||
 *  4. 赋值运算符：= += -= *= ～/= %= ??=
 *    a. ??= 若b没有值，则赋给右边的值；若b有值，则不赋值；
 */

void main() {
  // 算数元素符
  int a = 10;
  int b = 2;
  a++;
  print(a);

  // 关系运算符
  print(a == b);

  // 逻辑运算符
  bool isTrue = true;

  // 赋值运算符
  b ??= 10;
  print(b);
}